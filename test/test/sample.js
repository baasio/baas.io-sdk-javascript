describe('Array', function() {
	before(function() {});

	describe('#indexOf()', function() {
		it('should return -1 when not present', function() {
			[1,2,3].indexOf(4).should.equal(-1);
			// expect([1,2,3].indexOf(4)).to.equal(-1);
			// expect(1).to.equal(1);
		});
		
		it('should return -1 when not present', function() {
			[1,2,3].indexOf(4).should.equal(-1);
		});
	});
});